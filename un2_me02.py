# 03 ingressos adicionais: 75% de desconto (no ingresso do cliente)
# 04 ou mais ingressos adicionais: 100% de desconto (no ingresso do cliente)
# O desconto do combo é cumulativo ao desconto de faixa etária.
# Ao final o sistema deve gerar as seguintes informações:

# a) A relação nominal dos clientes com 100% de desconto combo no seu ingresso; (0,2)
# b) A quantidade de ingressos adicionais que foram comprados; (0,2)
# c) O percentual de clientes que tiveram desconto combo de 25%; (0,2)
# d) A média do valor de ingressos pagos por clientes com combo (considerando apenas o ingresso do cliente). (0,2)

