# 6 a 12 anos: 8% de desconto
# 13 a 25 anos: 5% de desconto
# Acima de 60 anos: 15% de desconto
# O valor normal do ingresso foi de R$20,00. O dono do circo percebeu que houve uma redução no número de clientes por espetáculo e decidiu continuar com a promoção da semana de estreia e divulgou a possibilidade de compra de ingressos em combo. Com isso, além de solicitar o nome e idade de cada cliente, o programa deve solicitar a quantidade de ingressos (além do cliente) do seu combo. O sistema deve solicitar o nome e idade das pessoas que irão com o cliente para o circo (0,2). O sistema deve aplicar os descontos abaixo no ingresso do cliente que indicou a opção de combo:
